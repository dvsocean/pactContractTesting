package objects;

public class Person {

  private String name;
  private String surname;
  private String profession;
  private int id;

  public String getName() {
    return name;
  }

  public String getSurname() {
    return surname;
  }

  public String getProfession() {
    return profession;
  }

  public int getId() {
    return id;
  }




}
